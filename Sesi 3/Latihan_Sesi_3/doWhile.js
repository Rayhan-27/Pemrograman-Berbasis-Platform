let val = 3;

do{
    console.log(val);
    val--;
}
while(val >= 1);

/* 
val = 3 itu adalah inisialisasi variabel val dengan nilai 3
val >= 1 itu adalah kondisi dimana selama val lebih besar dari atau sama dengan 1 maka perulangan akan terus berjalan
val-- itu adalah decrement dimana setiap kali perulangan selesai maka nilai val akan berkurang 1
do while akan menjalankan do terlebih dahulu baru mengecek kondisinya, dan minimal akan dijalankan sekali
*/