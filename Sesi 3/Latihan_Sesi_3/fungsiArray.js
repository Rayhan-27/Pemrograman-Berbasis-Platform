let fruits = ["Apple", "Watermelon", "Banana", "Orange"];

console.log(fruits);

fruits.pop(); //menghapus index terakhir dari array fruits
console.log(fruits);

fruits.push("Pineapple"); //menambahkan index terakhir dari array fruits
console.log(fruits);

fruits.shift(); //menghapus index pertama dari array fruits
console.log(fruits);

fruits.unshift("Strawberry"); //menambahkan index pertama dari array fruits
console.log(fruits);